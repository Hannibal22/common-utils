package com.easycms.common.poi;

import java.io.Serializable;

public class FileInfo implements Serializable{

	/** 
	 * 
	 */
	private static final long serialVersionUID = 1309929409013846568L;
	private String filename;	//文件名称不含后缀
	private String ralativePath;	//文档相对路径
	private String absolutePath;	//文档绝对路径
	private String size;	//文档大小
	private String type;	//文档类型
	public String getFilename() {
		return filename;
	}
	public void setFilename(String filename) {
		this.filename = filename;
	}
	public String getRalativePath() {
		return ralativePath;
	}
	public void setRalativePath(String ralativePath) {
		this.ralativePath = ralativePath;
	}

	public String getAbsolutePath() {
		return absolutePath;
	}
	public void setAbsolutePath(String absolutePath) {
		this.absolutePath = absolutePath;
	}
	public String getSize() {
		return size;
	}
	public void setSize(String size) {
		this.size = size;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public static long getSerialversionuid() {
		return serialVersionUID;
	}

}
