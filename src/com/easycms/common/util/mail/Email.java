package com.easycms.common.util.mail;

import java.util.Date;
import java.util.List;
/**
 * 邮件信息类
 * @author jiepeng
 *
 */
public class Email {
	private String addressFrom;	//邮件发送者
	private List<String> addressTo;	//邮件接收者
	private List<String> addressCc;	//明抄送
	private List<String> addressBcc;	//按抄送
	public List<String> getAddressCc() {
		return addressCc;
	}
	public void setAddressCc(List<String> addressCc) {
		this.addressCc = addressCc;
	}
	public List<String> getAddressBcc() {
		return addressBcc;
	}
	public void setAddressBcc(List<String> addressBcc) {
		this.addressBcc = addressBcc;
	}
	private String subject;		//邮件主题
	private Date sendDate;		//邮件发送时间
	private String content;		//邮件主要内容
	public String getAddressFrom() {
		return addressFrom;
	}
	public void setAddressFrom(String addressFrom) {
		this.addressFrom = addressFrom;
	}
	public List<String> getAddressTo() {
		return addressTo;
	}
	public void setAddressTo(List<String> addressTo) {
		this.addressTo = addressTo;
	}
	public String getSubject() {
		return subject;
	}
	public void setSubject(String subject) {
		this.subject = subject;
	}
	public Date getSendDate() {
		return sendDate;
	}
	public void setSendDate(Date sendDate) {
		this.sendDate = sendDate;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
}
